#Subscripting
print("Hello"[0]) #H
print("Hello"[4]) #o    

#String
print("Hello"[0:3]) #Hel
print("Hello"[1:4]) #ell
print("123" + "345") #123345

#Integer
print(123 + 345) #468

#Float
print(3.14 + 4.56) #7.7

#Boolean
print(True) #True
print(False) #False

#larger datatype
print(123_456_789) #123456789

#Type function returns the datatype of the value
print(type(123)) #<class 'int'>
print(type(3.14)) #<class 'float'>
print(type("Hello")) #<class 'str'>
print(type(True)) #<class 'bool'>   